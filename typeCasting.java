public class typeCasting {
    public static void main(String[] args) {
        int i=375;
        double d=i;
        System.out.println(i);
        System.out.println(d);
          

        double dd=345.66;
        int ii= (int)dd;
        System.out.println(dd);
        System.out.println(ii);

    }
}
