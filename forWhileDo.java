public class forWhileDo {
    public static void main(String[] args) {
        for (int i=0;i<0;i++){
            System.out.println(i);
        }
        int i=0;
        while (i<0){
            System.out.println(i);
            i++;
        }
        int a=1;
        do{
            System.out.println(a);
            a++;
        }while(a<0);
    }
}
