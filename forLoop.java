public class forLoop {
    
   
    public static void main(String[] args) {
        System.out.println("Numbers From 1 to 10");
        for (int i=1;i<=10;i++){
            System.out.println(i);
        }
        System.out.println("Numbers From 10 to 1");
        for (int i=10;i>=1;i--){
            System.out.println(i);
        }

        
        }
}

