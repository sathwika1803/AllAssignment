package polymorphism.encapsulation;

public class student {
    private String name;
    private int marks;
    public String getName() {
        return name;
    }
    public int getMarks() {
        return marks;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setMarks(int marks) {
        this.marks = marks;
    }
    
}
