public class arithmetic {
    public static void main(String[] args) {
        double a=10;
        double b=20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println((a==b)&&(a<b));
        System.out.println((a!=b)&&(a>b));
        System.out.println((a==b)||(a<b));
        System.out.println((a!=b)||(a>b));
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(a);
        System.out.println(--a);

        
    }
}
