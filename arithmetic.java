public class arithmetic {
    public static void main(String[] args) {
        double a=10;
        double b=20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println((a==b)&&(a<b));
        System.out.println((a!=b)&&(a>b));
        System.out.println((a==b)||(a<b));
        System.out.println((a!=b)||(a>b));
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(a);
        System.out.println(--a);
        a+=a;
        System.out.println(a);
        b-=b;
        System.out.println(b);
        a*=2;
        System.out.println(a);
        b/=2;
        System.out.println(b);
        a%=2;
        System.out.println(a);



        
    }
}
