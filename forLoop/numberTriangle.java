package forLoop;

public class numberTriangle {
    static int a=5;
    
    public static void main(String[] args) {
        
        for(int i=1;i<=a;i++){
            for(int j=1;j<=i;j++){
                
                System.out.print(j);
                
            }
           
            
            System.out.println();
        }
    }
}
