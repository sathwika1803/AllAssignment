public class switchdays {
    static char grade =2;
    public static void main(String[] args) {
        switch(grade){
            case 1:
                System.out.println("Excellent");
                break;
            case 2:
                System.out.println("Good");
                break;
            case 3:
                System.out.println("Average");
                break;
            case 4:
                System.out.println("Below Average");
                break;
            case 5:
                System.out.println("Fail");

        }
    }
}
